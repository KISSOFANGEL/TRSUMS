package com.trsnj.ums.entity;

/**
 * 定义树形属性对象
 * @author  dzy
 * @version  [V1.00, 2014-12-8]
 * @see  [相关类/方法]
 * @since V1.00
 */
public class TreeAttribute
{
    private String href;//href的属性值

    public TreeAttribute(){}
    
    public TreeAttribute(String href){
        this.href=href;
    }
    /**
     * 获取 href
     * @return 返回 href
     */
    public String getHref()
    {
        return href;
    }

    /**
     * 设置 href
     * @param 对href进行赋值
     */
    public void setHref(String href)
    {
        this.href = href;
    }
    
}
